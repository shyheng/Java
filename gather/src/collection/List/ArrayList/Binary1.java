package collection.List.ArrayList;
/*
* 位运算
* */
public class Binary1 {
    public static void main(String[] args) {
        //    5
//    >>1二进制右移一位
//    >> 2 二进制右移两位
//    10的二进制是00001010{10}
//    10的二进制右移一位是00000101（5）
        System.out.println(10>>1);


//        二进制位左移一位'
//    10的二进制是00001010{10}
//        10的二进制左移00010100(20)
        System.out.println(10<<1);
    }

}
